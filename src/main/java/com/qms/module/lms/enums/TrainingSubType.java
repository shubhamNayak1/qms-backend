package com.qms.module.lms.enums;

/**
 * Sub-classification within a TrainingType.
 *
 *  TEMPORARY   — One-off or short-term training (e.g. project-specific).
 *  REGULAR     — Recurring scheduled training cycle.
 *  REFRESHER   — Periodic refresh for staff who have previously completed the training.
 *  OJT         — On-the-Job Training; practical, shop-floor based.
 */
public enum TrainingSubType {
    TEMPORARY,
    REGULAR,
    REFRESHER,
    OJT
}
